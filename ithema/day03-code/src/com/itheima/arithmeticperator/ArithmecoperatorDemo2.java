package com.itheima.arithmeticperator;

public class ArithmecoperatorDemo2 {
    public static void main(String[] args) {
        // 除法
        // 1.整数参与计算只能是整数
        // 2.小数参与运算,结果可能不精确
        System.out.println(10/2);
        System.out.println(10.0/3);//3.3333333333333335
        // 取余
        System.out.println(10%3);
    }
}
